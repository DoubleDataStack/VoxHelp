local luamath = dofile("luamath.lua")

local function p(k, s, v)
	s = tostring(s)
	v = tostring(v)
	print(k..": src: "..s.." out: "..v)
end

local function math_print(m)
	p("abs", 10, m.abs(10))
	p("acos", 0.2, m.acos(0.2))
	p("asin", 0.2, m.asin(0.2))
	p("atan2", "0.2, 0.3", m.atan2(0.2, 0.3))
	p("atan", 0.2, m.atan(0.2))
	p("ceil", 23.3, m.ceil(23.3))
	p("cosh", 0.8, m.cosh(0.8))
	p("cos", 0.9, m.cos(0.9))
	p("deg", 23, m.deg(23))
	p("exp", 91, m.exp(91))
	p("floor", 83.9, m.floor(83.9))
	p("fmod", "152, 123", m.fmod(152, 123))
	p("frexp", 823, m.frexp(823))
	p("ldexp", "124, 132", m.ldexp(124, 132))
	p("log10", 762, m.log10(762))
	p("log", "912, 532", m.log(912, 532))
	p("max", "23, 73", m.max(23, 73))
	p("min", "891, 12", m.min(891, 12))
	p("modf", 12.123, m.modf(12.123))
	p("pow", "3, 3", m.pow(3, 3))
	p("rad", 90, m.rad(90))
	p("random", "", m.random())
	p("random(min)", 12, m.random(12))
	p("random(min,max)", 12, 24, m.random(12, 24))
	p("randomseed", 7123, pcall(m.randomseed, 7123))
	p("sinh", 0.23, m.sinh(0.23))
	p("sin", 0.72, m.sin(0.72))
	p("sqrt", 7523, m.sqrt(7523))
	p("tanh", 0.232, m.tanh(0.232))
	p("tan", -0.8723, m.tan(-0.8723))
end

print("lmathlib.c")
math_print(math)

print("\n\n\n\n")

print("luamath.lua")
math_print(luamath)